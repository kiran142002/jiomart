    private void otp_sendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_otp_sendActionPerformed
        // TODO add your handling code here:
          String em=remail.getText();
        String regex="^[a-zA-Z0-9]-(?:\\.[a-z]+)*@(?:\\.[a-z]+)+[a-z]{2,3}$";
        Pattern pattern=Pattern.compile(regex);
        Matcher matcher=pattern.matcher(em);
                if(matcher.matches()){
                       JOptionPane.showMessageDialog(this,"VALID EMAIL");
                }else{
                       JOptionPane.showMessageDialog(this,"IN-VALID EMAIL");
                }
                if(!em.isEmpty()){
                    generatedOTP=generateOTP();
                    sendOTPEmail(em,generatedOTP); 
                     JOptionPane.showMessageDialog(this,"OTP SEND TO: "+em);
                }else{
                     JOptionPane.showMessageDialog(this,"PLEASE ENTER A VALID EMAIL");
                }
    }//GEN-LAST:event_otp_sendActionPerformed

