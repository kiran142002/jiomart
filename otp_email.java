    private void sendOTPEmail (String recipientEmail,String otp){
          String senderEmail=semail.getText();
          String senderPassword=password.getText();
         
         
           Properties props=new Properties();
                props.put("mail.smtp.host", "smtp.gmail.com");
                props.put("mail.smtp.port", "587");
                props.put("mail.smtp.auth", "true"); 
                props.put("mail.smtp.starttls.enable", "true");
                
                
           
           Session session =Session.getInstance(props, new javax.mail.Authenticator() {
                @Override
                protected PasswordAuthentication getPasswordAuthentication(){
                    return new PasswordAuthentication(senderEmail,senderPassword);
                }
                    
           });
           try{
            Message message = new MimeMessage(session);

              message.setFrom(new InternetAddress(senderEmail));
              message.addRecipient(Message.RecipientType.TO,new InternetAddress(recipientEmail));
              message.setSubject("YOUR OTP CODE");
              
            
              message.setText("Your OTP CODE: "+otp);
             
              Transport.send(message);
              
               JOptionPane.showMessageDialog(this, "OTP SEND SUCCESSFULLY!");
           } catch (Exception e) {
                 JOptionPane.showMessageDialog(this,e);

           }
        
    }
    private String generateOTP(){
        int otp1=(int)(Math.random()*900000)+100000;
        return String.valueOf(otp1);
        
    }
